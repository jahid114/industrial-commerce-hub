export const getProduct = (id: string) => products.find((p) => p.id === id);
export const getProductsByCategory = (categoryId: string) => products.filter((p) => p.categoryId === categoryId);
export const getProductsByBrand = (brandId: string) => products.filter((p) => p.brandId === brandId);
export const getFeaturedProducts = () => products.filter((p) => p.featured);
export const getRelatedProducts = (id: string, limit = 4) => {
  const product = getProduct(id);
  if (!product) return [];
  return products.filter((p) => p.id !== id && p.categoryId === product.categoryId).slice(0, limit);
};
